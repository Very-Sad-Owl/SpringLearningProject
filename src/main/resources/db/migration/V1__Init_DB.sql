create sequence hibernate_sequence start 122 increment 1;

create table orders (
    id int8 generated by default as identity,
    address varchar(255),
    city varchar(255),
    date date,
    email varchar(255),
    first_name varchar(255),
    last_name varchar(255),
    phone_number varchar(255),
    post_index int4 not null check (post_index>=5),
    total_price float8,
    user_id int8,
    primary key (id)
);

create table orders_tea_list (
    order_id int8 not null,
    tea_list_id int8 not null,
    tea_list_order int4 not null,
    primary key (order_id, tea_list_order)
);

create table tea (
    id int8 not null,
    country varchar(255),
    description varchar(255),
    filename varchar(255),
    fragrance_base_notes varchar(255),
    tea_title varchar(255),
    brand varchar(255),
    price int4 not null,
    type varchar(255),
    volume varchar(255),
    primary key (id)
);

create table user_role (
    user_id int8 not null,
    roles varchar(255)
);

create table usr (
    id int8 not null,
    activation_code varchar(255),
    active boolean not null,
    email varchar(255),
    password varchar(255) not null,
    username varchar(255) not null,
    primary key (id)
);

create table usr_tea_list (
    user_id int8 not null,
    tea_list_id int8 not null
);

